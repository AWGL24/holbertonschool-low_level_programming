#include "3-calc.h"
/**
 * op_add - function that returns the sum of a and b
 * @a: first num
 * @b: second num
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that subtracts a and b
 * @a: first num
 * @b: second num
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that multiplies 2 numbers
 * @a: first num
 * @b: second num
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divides 2 numbers
 * @a: first number
 * @b: second number
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that returns the remainder of a and b
 * @a: first num
 * @b: second num
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
